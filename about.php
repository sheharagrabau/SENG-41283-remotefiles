        <p class="topic">Our services include:</p>  <ul>
        <li>Collection of blood within hospital based blood bank premises and in the community via mobile blood collection sessions</li>
        <li>Screening collected blood for transfusion transmissible infections, processing whole blood into components and storage of blood and blood components under optimal conditions</li>
        <li>Distribution and issue of blood and blood components within blood banks and efficient inventory management</li>
        <li> Promotion of appropriate clinical use of blood and blood components through hospital transfusion committees and by continuous educational programs</li>
        <li>Clinical services therapeutic plasma exchange, <abbr title="Human Leukocyte Antigen">HLA</abbr> typing and provision of <abbr title="Human Leukocyte Antigen">HLA</abbr> matched products on request, reference immunohaematology laboratory</li>
        <li>Manufacturing laboratory reagents and reagent red cells</li>
        <li>Conducting training programs for post graduate trainees on Transfusion Medicine and Haematology</li>
            
        
