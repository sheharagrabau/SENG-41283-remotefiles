    <h1><span class="capitaltopic">C</span>luster system</h1>
    NBTS consists of 96 hospital based blood banks affiliated to 19 cluster centers based on their geographic location. National Blood Center at Elvitigala Mawatha Narahenpita is the operational headquarters where national policies are developed and implemented for the efficient operation of blood bank network in the country.<br>
    These cluster centers themselves act as blood banks.
    
    <br><br>
    You can donate your blood by personally visiting us at one of the blood banks or by <a href="appointment.php" class="pagelink">making an appointment</a> prior to donation. Donation can also be done at donor camps (also known as mobile blood collection sessions) organized by many levels of non-profit organiztions. NBTS is the major service contributor at donor camps and is ever ready to give full guidance in such social services<br><br> 
    
