        <h1><span class="capitaltopic">H</span>ow often can I give blood?</h1>
        
        Men can give blood every 12 weeks and women can give blood every 16 weeks. If you have a health condition, have travelled out of the country recently, or if you answer "yes" to any question on your Donor Health Check questionnaire, please call the donor section +94117220677 for further advice about whether this affects you donating blood. <br><br>
        
        <b>The common reasons why donors should check if they can give blood are:</b>
        
        <ul>
            <li>if you are receiving medical or hospital treatment</li>
            <li>if you are taking medication</li>
            <li>after travelling overseas</li>
            <li>after having a tatoo or pirecing</li>
            <li>during and after pregnancy</li>
            <li>if you fell ill</li>
            <li>if you have cancer</li>
            <li>after receiving blood, blood products or organs</li>
            
        </ul>
        
        <h1><span class="capitaltopic">T</span>ypes of Donors</h1>
        
        <ul>
            <li>Voluntory non remunerated donors. (donate for the sake of others and do not expect any benefit. their blood is considered safe and healthy)</li>
            <li>Replacement donors. (donate to replace the units used for their friends or family members)</li>
            <li>Paid donors. (receive payment for donation)</li>
            <li>Directed donors. (donate only for a specific patient's requirement)</li>
        </ul>
        
        Replacement and Paid donors are no longer accepted by NBTS. Replacement donation was carried out until recently in some blood banks due to the shortage of blood stocks. Asking patients for replacement donors will lead to more pressure on the relatives, who are already in stress and this in turn results in illegal paid donations.<br>

Directed donations are used in certain conditions such as in rare blood groups.<br><br>

