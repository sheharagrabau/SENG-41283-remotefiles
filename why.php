    <h1><span class="capitaltopic">W</span>hy give blood?</h1>

    Giving blood saves lives. The blood you give is a lifeline in an emergency and for people who need long-term treatments. Many people would not be alive toady if donors had not generously given their blood.
    <br>

    <h1><span class="capitaltopic">W</span>hy we need you to give blood</h1>

    We need over 1,000 blood donations evey day to treat patients in need across Sri Lanka. Which is why there is always a need for people to give blood.

    Each year we need approximately 30,000 new donors, as some donors can no longer give blood. <br><br>

    Around half our current donors are over 45. That is why we need more young people (over the age of 17) to start giving blood, so we can make sure we have enough blood in the future. Find out who can give blood, and then <a href="appointment.php" class="pagelink">book an appointment</a> to give blood.
    <br><br>

    Remember, if you ever need to cancel a donation appointment we ask that you give us 3 days' notice so that we can offer your appointment to another donor. You can easily cancel or reschedule by reaching us via +94112369931-4

    <br>

    <h1><span class="capitaltopic">T</span>he need for blood</h1>

    We need to make sure that we have enough supplies of all blood groups and blood types to treat all types of conditions. By giving blood, every donor helps us meet the challenge of providing life-saving products whenever and wherever they are needed.<br><br>

    <a href="appointment.php" class="pagelink">Make an appointment</a> today to give blood or simply drop at one of our <a href="where.php" class="pagelink">blood banks</a> and help someone in need today.


    <br>
    
    <h1><span class="capitaltopic">M</span>aintaining the blood supply</h1>
    
    We need to maintain a regular supply of all blood groups and types so we can provide it to the hospitals and patients who need it. <br><br>
    
    We sometimes need to target specific blood types to increase stock levels. That's why we sometimes contact regular donors with the particular blood type we need, and ask them to give blood.<br><br>

    We are indebted to our regular blood donors for their role in helping us to save lives.
    
    
    <h1><span class="capitaltopic">R</span>are blood types</h1>
    
    A rare blood type is any blood type that is difficult to find in the population where you may need that "rare" type of blood.

    O Rh negative blood is rare but essential because it is the only blood type that can be given to anyone, regardless of their blood type.
        
